package com.wxggt.formbean;

public class RegisterCount {
	private String year_;
	private String month_;
	private String day_;
	private int registerCount_;

	public String getYear_() {
		return year_;
	}

	public void setYear_(String year_) {
		this.year_ = year_;
	}

	public String getMonth_() {
		return month_;
	}

	public void setMonth_(String month_) {
		this.month_ = month_;
	}

	public String getDay_() {
		return day_;
	}

	public void setDay_(String day_) {
		this.day_ = day_;
	}

	public int getRegisterCount_() {
		return registerCount_;
	}

	public void setRegisterCount_(int registerCount_) {
		this.registerCount_ = registerCount_;
	}

}
